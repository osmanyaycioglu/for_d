package com.ford.calc;

public class HesapMakinesi3 {
	public int deger1;
	public int deger2;
	public int sonuc;

	public void topla() {
		sonuc = deger1 + deger2;
	}

	public void cikar() {
		sonuc = deger1 - deger2;
	}

	public void carp() {
		sonuc = deger1 * deger2;
	}

	public void bol() {
		sonuc = deger1 / deger2;
	}

}
