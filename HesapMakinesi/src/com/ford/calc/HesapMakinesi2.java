package com.ford.calc;

public class HesapMakinesi2 {

	public int topla(int value1, int value2) {
		return value1 + value2;
	}

	public int cikar(int value1, int value2) {
		return value1 - value2;
	}

	public int carp(int value1, int value2) {
		return value1 * value2;
	}

	public int bol(int value1, int value2) {
		return value1 / value2;
	}

}
